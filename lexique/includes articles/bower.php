  <h3>BOWER</h3>
  <p>
    <strong>Bower</strong>
     fait partie d'une panoplie de services de nouvelle génération destinés à automatiser un maximum de tâches à votre place. Le but de cet outil développé par l'équipe de Twitter est de gérer les dépendances de votre projet.
  </p>
  <p>
     C'est quoi, une dépendance ? Lorsque vous débutez un nouveau projet, votre première tâche est de créer (ou de copier-coller) l'arborescence de vos fichiers et dossiers. Votre mission suivante est d'aller visiter un par un les outils incontournablespour votre projet : jQuery, Modernizr, Bootstrap, etc. et d'installer les nouvelles versions de ces outils… ou alors de mettre à jour vos propres modules les uns après les autres. Avouez que vous gagneriez du temps à ne plus vous soucier dece genre de tâches répétitives quotidiennes. Eh bien figurez-vous que ça tombe bien : tous les outils tels que jQuery, Modernizr ou Bootstrap sont des dépendances que
    <strong>Bower</strong>
     va vous aider à installer en une ligne de code !
  </p>
  <p>
     La liste des dépendances inscrites dans le registre de
    <strong>Bower</strong>
     est pour le moins impressionnante, et parmi lesquelles vous trouverez un bon nombre de célébrités :
  </p>
  <ul>
    <li>Twitter Bootstrap</li>
    <li>jQuery</li>
    <li>jQuery mobile</li>
    <li>html5boilerplate</li>
    <li>font-awesome</li>
    <li>modernizr</li>
    <li>html5shiv</li>
    <li>normalize</li>
    <li>grunt</li>
    <li>purecss</li>
    <li>respond</li>
    <li>raphael</li>
    <li>zepto</li>
    <li>phonegap</li>
    <li>yepnope</li>
    <li>… knacss</li>
  </ul>
  <p>
     Deux pré-requis sont nécessaires au bon fonctionnement de
    <strong>Bower</strong>
     : Node.js (et son manager NPM, Node Packaged Modules) et Git.
  </p>
  <p>
     Bien que pratique et rapide,
    <strong>Bower</strong>
     n'en demeure pas moins un "simple" gestionnaire de dépendances : il n'est pas prévu pour construire des dossiers de projets complets, ni pour concaténer ou minifier des ressources. Pour ce genred'aspirations, et pour aller plus loin, il vous faudra lorgner du côté d'autres outils tels que Yeoman ou Grunt.
  </p>
