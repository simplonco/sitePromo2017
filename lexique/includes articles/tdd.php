  <h3>Le TDD</h3>
  <img src="../assets/img/TDD.png" alt="TDD" title="TDD" />
  <p>
    Le test-driven development ou en français développement piloté par les tests est une technique de développement de logiciel qui préconise d'écrire les tests unitaires avant d'écrire le code source d'un logiciel. Le cycle préconisé par TDDcomporte cinq étapes :
  </p>
  <ul>
    <li>écrire un premier test ;</li>
    <li>vérifier qu'il échoue (car le code qu'il teste n'existe pas), afin de vérifier que le test est valide;</li>
    <li>écrire juste le code suffisant pour passer le test ;</li>
    <li>vérifier que le test passe ;</li>
    <li>puis réusiner le code, c'est-à-dire l'améliorer tout en gardant les mêmes fonctionnalités.</li>
  </ul>
  <p>
    Ces tests permettent aussi de préciser les spécifications du code, et donc son comportement ultérieur en fonction des situations auxquelles il sera exposé. Ce qui facilite la production d'un code valide en toutes circonstances. On obtientdonc un code plus juste et plus fiable.En écrivant les tests d'abord, on utilise le programme avant même qu'il existe. On s'assure ainsi que le code produit est testable unitairement. De plus, le fait d'avoir des tests augmente la confianceen soi du programmeur lors du réusinage du code : il sait qu'à un moment donné les tests ont réussi. Le TDD est souvent associé, particulièrement dans la méthode XP, à la programmation en binôme. Pour expliquer les rôles des deux acteursimpliqués, il est généralement utilisé la métaphore du rallye automobile. Pour chaque fonctionnalité à réaliser, l'un des développeurs, nommé copilote, code le test. Ensuite, le pilote code la fonctionnalité elle-même. À chaque nouvellefonctionnalité ajoutée, l'ensemble test-code ainsi réalisé sera de nouveau exécuté, réduisant ainsi au maximum le risque de régression.
  </p>
