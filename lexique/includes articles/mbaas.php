  <h3>MBaas</h3>
  <img src="../assets/img/basse.jpg" alt="MBAAS" title="MBAAS">
  <h4>"Mobile backend as a service" ou "solution de back-end mobile à la demande"</h4>
  <br/>
  <p>
    C'est une architecture informatique hébergée dans le Cloud qui offre aux applications mobiles un accès à des dispositifs de stockage, à des bases de données et à d’autres ressources dont elles peuvent avoir besoin pour fonctionner.
  </p>
  <br/>
  <p>
    Cette approche de type PaaS a recours à des API et à des kits de développement logiciel (SDK) pour relier les applications mobiles au back-end Cloud.
  </p>
  <br/>
  <p>
    Une solution MBaaS peut servir à assurer des fonctions courantes, telles que la transmission des notifications, l'intégration à des réseaux sociaux ou les services de géolocalisation. Cette approche s'écarte du développement d'applicationsmobiles classique, qui oblige les développeurs à intégrer individuellement les API de chaque service dont l’app a besoin.
  </p>
  <br/>
  <p>
    Le modèle MBaaS permet également aux développeurs d'applications mobiles de s'abstraire complètement de l'infrastructure côté serveur inhérente à tout back-end. Il leur suffit d'assembler les constituants essentiels requis et d'écrire le codepermettant de les relier entre eux. Ils peuvent alors se consacrer pleinement au développement d'expériences utilisateur au lieu de se préoccuper des questions d’infrastructure sous-jacente.
  </p>
