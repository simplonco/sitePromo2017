  <h3>User Story</h3>
  <img src="../assets/img/UserStory.png"alt="UserStory" title="UserStory" />
  <p>Dans les méthodes agiles, un récit utilisateur ou user story est une phrase simple dans le langage de tous les jours permettant de décrire avec suffisamment de précision le contenu d'une fonctionnalité à développer.
  La phrase contient généralement trois éléments descriptifs de la fonctionnalité : Qui ? Quoi ? Pourquoi ?.</p>
  <h4>En tant que <qui>, je veux <quoi> afin de <pourquoi></h4>
  <p>Le pourquoi est optionnel. Il permet cependant d'identifier l'intérêt de la fonctionnalité.</p>
