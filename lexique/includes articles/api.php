  <h3>Définition : API</h3>
  <p>
    API est un acronyme pour Applications Programming Interface. Une API est une interface de programmation qui permet de se « brancher » sur une application pour échanger des données. Une API est ouverte et proposée par le propriétaire du programme.
    </br>
    Les API sont utilisées dans différents domaines, exemple: (du marketing digital).
    </br>
    Les API proposées par les programmes de liens commerciaux permettent par exemple de créer automatiquement des campagnes ou annonces à partir d’une base produits (Voir API Adwords).
    </br>
    Les API des plateformes d’emailing permettent d’alimenter automatiquement les campagnes à partir des données produits.
    </br>
    Les gros programmes d’affiliation ouvrent également des API d’affiliation pour permettre à certains affiliés de reprendre la totalité de leurs catalogues.
    </br>
    Pour un usage de données contextuelles ou servicielles, des API peuvent également être proposées sur un portail d’open data.
  </p>
