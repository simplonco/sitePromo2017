  <h3>Un Mock-up, qu'est-ce que c'est ?</h3>
  <img src="../assets/img/image13.png" alt="image13.png" title="MOCK UP" />
  <p>
    Un mockup est un terme informatique, et même un terme de design, qui désigne une maquette d'une interface utilisateur. Cette maquette se veut volontairement simpliste pour se fier principalement sur les fonctionnalités que sur l'aspect esthétiquefinal.
  </p>
  <h3>Présentation:</h3>
  <p>
    Un mockup représente un squelette d'une interface utilisateur. Ce squelette est en noir et blanc et à principalement pour but de montrer sur papier (ou sur ordinateur) où seront placé les éléments sur l'interface et comment fonctionnera l’interactionentre eux (bouton d'action, zone de texte, élément interactif ...).
  </p>
  <h3>Avantages:</h3>
  <p>
    Un mockup est une bonne méthode durant la phase de conception d'un site web, d'un logiciel ou de tout autre interface pour les raisons suivantes:
  </p>
  <ul>
    <li>Possibilité de montrer l'aperçu de l'interface à des collaborateurs</li>
    <li>Constater un problème de placement, de taille des éléments ou même une impossibilité technique</li>
    <li>Déplacer facilement un élément pour améliorer l'interface (expérience utilisateur, ergonomie ...)</li>
    <li>Mieux estimer la complexité d'une interface et donc meilleure estimation du temps de conception</li>
    <li>Modification rapide de l'interface avant même que les graphistes ou les développeurs n'ai commencé à travailler. Une modification en amont est plus rapide que pendant la phase d'intégration ou de développement.</li>
  </ul>
