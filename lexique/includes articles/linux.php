  </p>
  <h3>Ubuntu</h3>
  <p>
    Ubuntu (prononciation [u.bun.tu], « ou-boun-tou » en français) est un système d'exploitation open source basé sur la distribution Linux Debian. Son nom provient d'un ancien mot bantou qui signifie « je suis ce que je suis grâce à ce que noussommes tous ».
  </p>
  <p>
    Source:
    <a href=https://fr.wikipedia.org/wiki/Ubunt>Wikipédia Ubuntu</a>
  </p>
  <p>
    LTS signifie Long Term Support, ou support à long terme. certaines versions d'Ubuntu sont estampillées LTS. Ces versions, publiées tous les deux ans au mois d'avril, sont soutenues pour une durée prolongée de 60 mois (5 ans) pour les postesde travail et les serveurs.
  </p>
