  <h3>Linux</h3>
  <img src="../assets/img/logo_linux.jpg" alt="Logo Linux" title="LINUX" />
  <p>
    Linux est un système d'exploitation complet et libre, qui peut être utilisé en lieu et place de systèmes d'exploitation commercialisés, tels que Windows, de Microsoft. Il est accompagné de nombreux logiciels libres complémentaires, offrantun système complet aux utilisateurs.
  </p>
