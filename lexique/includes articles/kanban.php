    <h3>Kanban</h3>
    <img src="../assets/img/kanban.png" alt="logo Kanban" title="Rester enfoncé pour zoomer" />
    <h3>Définition</h3>
    <p>La méthode Kanban est utilisée afin de limiter les tâches-en-cours. Bien que le nom Kanban provienne du japonais, qu’il puisse être traduit comme "carte de signalisation",  ces cartes représentent plutôt des éléments de travail.</p>
    <p>Elle fait partie de la méthode agile, et permets une amélioration du travail collaboratif.</p>
    <p>Elle est basée sur 4 fondements: </p>
    <ul>
      <li>Commencer par ce que vous faites actuellement</li>
      <li>Accepter d’appliquer les changements évolutifs et augmentés</li>
      <li>Respecter le processus actuel, les rôles, les responsabilités et les titres</li>
      <li>Leadership à tous les niveaux</li>
    </ul>
