  <h3>POO : protected</h3>
  <img src="../assets/img/poo-protected.png" alt="image cadenas protected" title="POO protected" />
  <p>
    Une
    <strong>classe</strong>
     protected, est visible par la même classe ET celles qui en héritent et seulement celles qui en héritent, pas de l’exterieur.
  </p>
  <p>
    Une
    <strong>propriété</strong>
     peut être protected, elle est accessible depuis la même classe ET également depuis les classes qui en héritent. Pour l’héritage, la propriété private reste uniquement cantonné à la même classe et pas celle qui en hérite, protected permet de transférer les propriétés, elles peuvent être modifiées par les classes qui en héritent.
  </p>
  <p>
    Une
    <strong>méthode</strong>
     protected, est, elle, accessible depuis la même classe et toutes celles qui en héritent. C’est exactement le même principe que les propriétés.
  </p>
