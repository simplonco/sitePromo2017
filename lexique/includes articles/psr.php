  <h3>PSR</h3>
  <p>
    Les PSR (pour « Propose a Standards Recommendation ») sont des recommandations validées par le PHP Framework Interoperability Group, ayant pour objectif d’améliorer l’interopérabilité entre les frameworks PHP, et plus généralement entre lesdéveloppeurs PHP. Il ne s’agit aucunement de normes ou de standards établis mais de recommandations qui tendent à le devenir. D’un sens, les PSR sont similaires aux RFC (Request For Comment) qui régulent désormais le monde des réseaux.
  </p>
  <p>
    A ce jour, les recommandations PSR validées sont aux nombres de 7 :
  </p>
  <ul>
    <li>PSR-4 est la norme pour l’auto chargement des classes. PSR-1 étant déprécié.</li>
    <li>PSR-1 et PSR-2 vont se concentrer sur la forme du code</li>
    <li>PSR-3 est pour la mise en forme de log.</li>
    <li>PSR-5: PHPDoc porté par l'équipe de phpDocumentor</li>
    <li>PSR-6 pour la mise en cache d’objets (Redis, memcached)</li>
    <li>PSR-7 pour les requêtes/réponses HTTP</li>
  </ul>
  <p>
    Ces recommandations font débat dans la communauté. Vous pouvez lire du contre par là et par là. L’une des raisons est que ces recommandations qui, à la base, doivent traiter de l’interopérabilité entre frameworks PHP traitent naturellementde sujets sensibles, tels que les conventions de codage qui restent très subjectives. Reste que la démarche est intéressante et qu’il est toujours bon d’aller dans le sens d’une harmonisation des méthodes de développements.
  </p>
