  <h3>CRUD - Qu'est-ce que c'est ?</h3>
  <p>L'acronyme informatique anglais
    <strong>CRUD</strong>
    (pour create, read, update, delete) (parfois appelé SCRUD avec un "S" pour search) désigne les quatre opérations de base pour la persistance des données, en particulier le stockage d'informationsen base de données.</p>
  <p>Plus généralement, il désigne les opérations permettant la gestion d'une collection d'éléments. Ce terme est aussi un jeu de mot en anglais sur l'adjectif crude (en français brut ou rudimentaire). Voir aussi les noms REST, RESTful et HATEOAS.</p>
  <p>Soit :</p>
  <ul>
    <li>Create : créer</li>
    <li>Read : lire</li>
    <li>Update : mettre à jour</li>
    <li>Delete : supprimer</li>
  </ul>
  <h3>Utilisation dans les interfaces logicielles :</h3>
  <p>Par exemple, une application carnet d'adresse dont l'élément le plus simple est un contact, doit permettre à l'utilisateur de :</p>
  <ul>
    <li>Créer des contacts</li>
    <li>Lire un contact (liste, zone de recherche…)</li>
    <li>Mettre à jour un contact</li>
    <li>Supprimer un contact</li>
    <li>Si l'une ou l'autre de ces opérations fondamentales manque, le logiciel sera considéré comme incomplet.</li>
  </ul>
