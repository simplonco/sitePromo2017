  <h3>GRUNT - Qu'est-ce que c'est ?</h3>
  <p>
    <strong>Grunt</strong>
     est un outil d’exécution de commandes en Javascript. Les intégrateurs web doivent minifier les fichiers avant mise en production, réduire le poids des images, concatener le code, etc. Eh bien Grunt est un task runner, c'està dire qu'il effectue ces tâches pour vous.
  </p>
  <p>
    Grunt fonctionne avec Node.js (cf -def: NPM):
  </p>
  <ul>
    <li>Premièrement installer Grunt: <code>npm install -g grunt-cli</code></li>
    <li>Concaténer les fichiers avec le plug-in: <code>npm install grunt-contrib-concat --save-dev</code></li>
    <li>Minifier JavaScript avec le plug-in: <code>npm install grunt-contrib-uglify --save-dev</code></li>
    <li>Optimisation des images avec le plug-in: <code>npm install grunt-contrib-imagemin --save-dev</code></li>
  </ul>
