  <h3>NPM: The Node Package Manager</h3>
  <img src="../assets/img/npm_on-site_logo_large.png" alt="NPM" title="NPM">
  <p>
    NPM est le gestionnaire de paquets officiel de Node.js. Depuis la version 0.6.3 de Node.js, NPM fait partie de l'environnement et est donc automatiquement installé par défaut3. NPM fonctionne avec un terminal et gère les dépendances pour uneapplication. Il permet également d'installer des applications Node.js disponibles sur le dépôt NPM.
  </p>
  <p>
    Exemple:
    <code>
      $ npm install bootstrap@3
    </code>
  </p>
