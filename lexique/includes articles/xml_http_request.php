  <h3>XMLHttpRequest</h3>
  <img src="../assets/img/xhr.jpg" alt="XHR" title="XHR" />
  <p>
    XMLHttpRequest(souvent abrégé XHR), est un objet du navigateur accessible en javascript, qui permet d'obtenir des données au format HTML, JSON, XML ou même un simple texte a l'aide de requête HTTP.
  </p>
  <p>
    AJAX permet de faire un erquête par le biais du JS.
