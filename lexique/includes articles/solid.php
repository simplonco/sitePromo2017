  <h3>SOLID</h3>
  <img src="../assets/img/SOLID.jpg" alt="schema solid" title="SOLID">
  <p>
    SOLID est un acronyme représentant cinq principes de bases pour la programmation orientée objet, introduits par Michael Feathers et Robert C. Martin au début des années 2000. Ils sont censés apporter une ligne directrice permettant le développementde logiciel plus fiable et plus robuste.
  </p>
  <p>
    En français :
  </p>
  <ul>
    <li><strong>Responsabilité unique (SingSubstitution de Liskov (Liskov substitution Principle):le Responsibility Principle):</strong> une classe, une fonction ou une méthode doit avoir une et une seule responsabilité</li>
    <li><strong>Ouvert/fermé (Open/closed principle):</strong>une classe doit être ouverte à l'extension, mais fermée à la modification </li>
    <li><strong>Substitution de Liskov (Liskov substitution Principle):</strong> une instance de type T doit pouvoir être remplacée par une instance de type G, tel que G sous-type de T, sans que cela ne modifie la cohérence du programme</li>
    <li><strong>Ségrégation des interfaces (Interface segregation principle):</strong> préférer plusieurs interfaces spécifiques pour chaque client plutôt qu'une seule interface générale </li>
    <li><strong>Inversion des dépendances (Dependency Inversion Principle):</strong> il faut dépendre des abstractions, pas des implémentations </li>
  </ul>
