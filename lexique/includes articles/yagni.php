  <h3>YAGNI</h3>
  <p>
    YAGNI (anglicisme, acronyme anglais de you ain't gonna need it, qui peut se traduire par « vous n'en aurez pas besoin ») est un principe d'extreme programming qui déclare que les programmeurs ne devraient pas ajouter de fonctionnalité à un logicieltant que celle-ci n'est pas absolument nécessaire.
  </p>
  <p>
    Ecrire du code qui n'est pas nécessaire à l'instant présent, mais qui pourrait l'être dans le futur, entraine des inconvénients parmi lesquels :
  </p>
  <ul>
    <li>Une perte de temps lors de l'ajout, du test ou de l'amélioration de fonctionnalités par rapport à celles qui sont immédiatement nécessaires.</li>
    <li>Possibilité d'un conflit avec une future fonctionnalité nécessaire.</li>
    <li>Tant que la fonctionnalité n'est pas réellement nécessaire, il est difficile de définir complètement ce qu'elle doit faire, et comment la tester. Si cette nouvelle fonctionnalité n'est pas correctement définie et testée, elle risque de ne pasfonctionner correctement lorsqu'elle sera un jour nécessaire.</li>
    <li>Ecriture de code inutilement long. Le logiciel grossit et devient plus compliqué.</li>
    <li>La fonctionnalité peut être inconnue des programmeurs qui pourraient s'en servir.</li>
  </ul>
  <p>
    L'extreme programming a été inventée en mars 1996, et est née officiellement en octobre 1999 avec le livre Extreme Programming Explained de Kent Beck (chef de projet).
  </p>
