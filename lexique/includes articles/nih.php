  <h3 class="mot">NIH</h3>
  <img src="../assets/img/nih.jpg" alt="NIH" title="NIH" />
  <p>
    L'expression not invented here (littéralement « pas inventé ici » ou de manière développée « ce truc n'a pas été développé par nous ») désigne de manière péjorative l'attitude d'une entreprise ou une association qui re-développe en internetout ou partie d'un logiciel ou d'interfaces déjà développés ailleurs.
  </p>
