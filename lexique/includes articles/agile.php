  <h3>Agile</h3>
  <img src="../assets/img/agile.jpg" alt="image méthode Agile" title="méthode Agile"/>
  <p>Les méthodes agiles sont des pratiques de pilotage et de réalisation de projets. Les méthodes agiles se veulent plus pragmatiques que les méthodes traditionnelles, impliquent au maximum le demandeur (client) et permettent une grande réactivité à ses demandes. Elles reposent sur un cycle de développement itératif, incrémental et adaptatif et doivent respecter quatre valeurs fondamentales déclinées en douze principes desquels découlent une base de pratiques, soit communes, soit complémentaires.</p>
  <p>Les méthodes les plus utilisées actuellement sont le Scrum, et le  XP (Extreme programming)</p>
  <p>Les méthodes agiles se veulent respecter toujours ces 12 points :
    <ul>
        <li>  Satisfaire le client en priorité</li>
        <li>  Accueillir favorablement les demandes de changement</li>
        <li>  Livrer le plus souvent possible des versions opérationnelles de l’application</li>
        <li>  Assurer une coopération permanente entre le client et l’équipe projet</li>
        <li>  Construire des projets autour d’individus motivés</li>
        <li>  Privilégier la conversation en face à face</li>
        <li>  Mesurer l’avancement du projet en termes de fonctionnalités de l’application</li>
        <li>  Faire avancer le projet à un rythme soutenable et constant</li>
        <li>  Porter une attention continue à l’excellence technique et à la conception</li>
        <li>  Faire simple</li>
        <li>  Responsabiliser les équipes</li>
        <li>  Ajuster à intervalles réguliers son comportement et ses processus pour être plus efficace</li>
    </ul>
  </p>
  <p> Exemples de techniques agiles : TDD, pair-programming,...</p>
