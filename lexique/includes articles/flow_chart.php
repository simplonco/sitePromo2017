  <h3>Flow Chart</h3>
  <p>Le Flow chart ou organigramme de programmation est une représentation graphique normalisée de l'enchaînement des opérations et des décisions effectuées par un programme d'ordinateur. L'organigramme de programmation utilise des symboles normalisés.</p>
  <p>Le sens par défaut des liens du flux d'exécution est:</p>
  <ul>
    <li>du haut vers le bas pour les liens verticaux;</li>
    <li>de la gauche vers la droite pour les liens horizontaux.</li>
  </ul>
  <p>Lorsque le sens par défaut n'est pas respecté, il est nécessaire de le préciser par une flèche à l'extrémité du lien.</p>
