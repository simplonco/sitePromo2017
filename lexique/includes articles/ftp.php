  <h3>FTP</h3>
  <p>FTP veut dire « File Transfert Protocol » ou Protocole de transfert de Fichier. C'est un protocole de communication destiné au partage de fichiers sur un réseau TCP/IP.</p>
  <p>Il permet, depuis un ordinateur, de copier des fichiers vers un autre ordinateur du réseau, ou encore de supprimer ou de modifier des fichiers sur cet ordinateur.</p>
  <p>Il y a 2 intervenants dans un échange FTP : le serveur et le client.</p>
  <ul>
    Le serveur FTP:
    <li>Le serveur FTP est un logiciel qui va répondre aux demandes des clients. Lorsque le serveur reçoit une demande, il vérifie les droits et si le client à les droits suffisants, il répond à cette demande sinon la demande est rejetée.</li>
    Le client FTP:
    <li>Il se connecte au serveur FTP, effectue les commandes (récupération ou dépôt de fichiers) puis se déconnecte. Toutes les commandes envoyées et toutes les réponses seront en mode texte. Le protocole FTP n’est pas sécurisé : les mots de passesont envoyés sans cryptage entre le client FTP et le serveur FTP. (Le protocole FTPS avec S pour « secure » permet de crypter les données).</li>
  </ul>
