  <h3>Git - Qu'est-ce que c'est ?</h3>
  <p>
    <strong>Git</strong>
     est un logiciel de gestion de versions décentralisé. Logiciel libre, il a été créé par
    <strong>Linus Torvalds</strong>
    , auteur du noyau
    <strong>Linux</strong>
    , et distribué selon les termes de la licence publique généraleGNU version 2. En 2016, il s’agit du logiciel de gestion de versions le plus populaire qui est utilisé par plus de douze millions de personnes.La première version stable est sortie en Décembre 2005, soit moins d'un an après le début du développement.A bien des égards, Git peut être considéré comme un système de fichiers à part entière, possédant son propre système de versionnement. Avec le temps et la participation active de la communauté, Git se trouve doté de toutes les fonctionnalitésd'un logiciel de gestion de versions. On peut affirmer que Git est au développement logiciel ce que sont les sauvegardes/save points au jeu vidéo. En effet, dans une situation vidéo-ludique, les sauvegardes nous permettent de ne pas avoir peurdu futur, et d'avoir la possibilité de revenir en arrière à un point précis, et ce, n'importe quand. Dans le cadre d'un développement logiciel, Git fonctionne de la même façon. En effet, il intègre la notion de
    <strong>Snapshot</strong>
    .
  </p>
  <h3>Snapshot ou Commit</h3>
  <p>
    Un
    <strong>Snapshot</strong>
     ou
    <strong>Commit</strong>
     représente l'état au complet d'un projet à un instant t. Au cours du déroulement normal d'un projet, les fichiers sont, bien sûr, amenés à être modifiés, ajoutés ou supprimés, le projet adonc une vie. Les snapshot permettent donc de figer l'état d'un dossier à un moment précis dans le temps afin de pouvoir effectuer différentes opérations dessus. En effet on pourrait vouloir comparer les fichiers à deux moments différents dansle temps, ou bien même vouloir revenir en arrière. C'est à cela que servent les commits.
  </p>
