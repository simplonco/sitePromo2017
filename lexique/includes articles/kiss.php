    <h3>Kiss :</h3>
    <p>
      NON ce n'est pas le célèbre groupe de rock des années 70, kiss est une abreviation pour "
      <span>Keep It Stupid Simple</span>
      " ou "
      <span>Keep It Simple, Stupid..</span>
      "
      <br/>
      <br/>
       Qu'est-ce que ça veut dire ? littéralement, comprenez motà mot : « garde ça simple, idiot », dans le sens de « ne complique pas les choses »
      <br/>
      <br/>
       Ce principe à été un élement clef, et un énorme succès dans le domaine de l'ingénierie logicielle. Un problème courant chez les ingénieurset développeurs de logiciels aujourd'hui, c'est qu'ils ont tendance à compliquer les problèmes.
      <br/>
      <br/>
       En règle générale, lorsqu'un développeur est confronté à un problème, il le décompose en plus petits morceaux qu'il pense comprendre,puis tente de mettre en œuvre la solution dans le code. Je dirais que 8 ou 9 sur 10 développeurs font l'erreur qu'ils ne décomposent pas le problème en assez petit ou compréhensible morceaux. Cela a pour résultat des implémentationstrès complexes même des problèmes les plus simples, un autre effet secondaire est le code "spagetthi", quelque chose que nous pensons que seul BASIC pourait faire avec ses déclarations goto, mais en Java cela donne des classes de 500à 1000 lignes de code, dont chaque méthodes a plusieurs centaines de lignes..
      <br/>
      <br/>
       Ce désordre de code resulte des cas d'exception à sa solution originale que met en place le dévelopeur pendant qu'il écrit dans son code. Ces casd'exception auraient été résolu si le développeur avait décomposé le problème plus tôt.
    </p>
