  <h3>MoSCoW</h3>
  <img src="../assets/img/place-rouge_small.png" alt="MOSCOW" title="MOSCOW" />
  <p>
    La méthode de priorisation MoSCoW permet au porteur de projet et à l’équipe de réalisation de s’accorder sur l’importance des tâches à réaliser par rapport aux délais prévus. Cette méthode est proposée dans le framework de gestion de projetframework DSDM, et est simplement un moyen mnémotechnique pour se souvenir de 4 degrés de priorité différents.
  </p>
  <ul>
    <li>M</li>
    <li>MUST : DOIT être fait</li>
    <li>S</li>
    <li>SHOULD : DEVRAIT être fait dans la mesure du possible</li>
    <li>C</li>
    <li>COULD : POURRAIT être fait dans la mesure où cela n’a pas d’impact sur les autres tâches</li>
    <li>V</li>
    <li>WON’T : NE SERA PAS fait cette fois mais sera fait plus tard</li>
  </ul>
