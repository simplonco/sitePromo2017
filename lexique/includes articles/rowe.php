  <h3>Le ROWE - Qu'est-ce que c'est ?</h3>
  <img src="../assets/img/coucou.jpg" alt="Schema ROWE" title="ROWE" />
  <p>
    Le ROWE -Results Only Work Environment- pouvant se traduire par Environnement de Travail axé Uniquement sur les Résultats est une stratégie de management des ressources humaines dans laquelle les employés sont payés pour leurs résultats plutôtqu'au nombre d'heures de travail.
  </p>
  <p>
    Ses créateurs, Jody Thompson et Cali Ressler, ont tout d'abord proposé cette stratégie à Best Buy, s'avérant intéressante elle s'est faite connaître par nombre d'entreprises dont
    <a href="https://en.wikipedia.org/wiki/Gap_Inc." title="gap"target="_blank">gap</a>
     ou même
    <a href="https://en.wikipedia.org/wiki/Gap_Inc." title="Fairview Health Services I.T Department" target="_blank">Fairview Health Services I.T Department</a>
    .
  </p>
  <h3> Le ROWE - Qu'est-ce que ça permet ?</h3>
  <p>
    ROWE tente de fournir aux directeurs les outils leur permettant de définir des objectifs qui peuvent être clairement atteignables ou non en fonction des résultats de chaque contributeur travaillant pour eux.
  </p>
  <p>
    Cette focalisation sur la possibilité ou non d'atteindre des objectifs permet à la fois aux employés de connaître leurs limites et aux employeurs de connaître leurs possibilités, résultant en une liberté non-négligable donnée à l'organisationde se concentrer sur moins de détails de dernière minute dans l'organisation quotidienne de chaque employé.
  </p>
  <h3> Le ROWE - Comment ça évolue ?</h3>
  <p>
    Depuis sa création, un nombre en constante croissance d'entreprise se servent de cette stratégie. Et certaines expérimentent avec, comme par exemple la Suède qui a adopté cette stratégie avec uniquement 6 heures de travail par jour, dans l'espoird'améliorer la productivité des employés, créant des conditions de travail qui leurs sont plus saines.
  </p>
