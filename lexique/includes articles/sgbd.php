  <h3>SGBD: Système de gestion de base de données</h3>
  <img src="../assets/img/sgbd.png" alt="logo SGBD" title="Rester enfoncé pour zoomer" />
  <h3>Définition</h3>
  <p>C'est un logiciel qui permet de stocker des informations dans une base de données. Un tel système permet de lire, écrire, modifier, trier, transformer ou même imprimer les données qui sont contenus dans la base de données.</p>
  <p>Ce sont les logiciels intermédiaires entre les utilisateurs et les bases de données</p>
  <p>Les principaux SGBD sont MySQL, PostgreSQL, SQLite, Microsoft SQL Server</p>
  <p>Sources : <a href="http://sql.sh/sgbd" title="wikipedia" target="_blank">SQL.sh</a> </p>
