  <h3>SCRUM - ou l'apéro sans alcool.</h3>
  <img src="../assets/img/methode-scrum.jpg" alt="schema méthode scrum" title="SCRUM" />
  <p>
    Scrum est une méthode intégrante à la méthode Agile. Scrum a 4 valeurs fondamentales : L’équipe , l’application, la collaboration, l’acceptation du changement. L’approche est de définir un besoin. Ce besoin peut être interne ou externe maisc’est à vous de le gérer de la planification à la gestion du temps en passant par la gestion des obstacles.
  </p>
  <p>
    Les règles sont simples mais strictes : la durée du projet est découpée en itérations courtes. Une itération contient seulement une partie de l’application, complète ( développée et testée). Ca se déroule comme suit :
  </p>
  <ul>
    <li>Une réunion initiale commence chaque itération pour définir les tâches. Les itérations doivent être claires pour chacun.</li>
    <li>Chaque jour, une courte réunion de 5 minutes maxi permet à toute l’équipe d’avoir une vue globale du projet : de savoir ce qui s’y passe et comment ça se passe.</li>
    <li>Une réunion finale présente une démonstration de ce qui est fait avec ce qui s’est bien passé ou pas.</li>
    <li>Parallèlement à cela, des tableaux de bords sont régulièrement générés pour avoir une vue réelle de l’avancement (MyPads par exemple) durant la période de "sprint".</li>
  </ul>
  <p>
    Ainsi la notion de coût, de délais, de situation, de charge, de tests sont des éléments qui doivent être vus à tout moment.
  </p>
