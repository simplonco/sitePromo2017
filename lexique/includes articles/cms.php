  <h3>CMS</h3>
  <p>
    Un système de gestion de contenu ou SGC (content management system ou CMS en anglais) est une famille de logiciels destinés à la conception et à la mise à jour dynamique de sites Web ou d'applications multimédia.
  </p>
  <img src="../assets/img/CMS.png" alt="CMS" title="CMS" />
  <ul>
    Liste des principaux systèmes de gestion de contenu :
    <li>MediaWiki est un moteur de wiki</li>
    <li>Textpattern est un SGC Open Source en PHP</li>
    <li>eZ Publish est un SGC Open Source en PHP</li>
    <li>eXo Platform est un SGC Open Source en Java</li>
    <li>WordPress est un moteur de blog</li>
    <li>Dotclear est un moteur de blog</li>
    <li>Magento est un moteur de boutique en ligne</li>
    <li>Magnolia est un SGC OpenSource Java</li>
    <li>Joomla! est un moteur de portails</li>
    <li>Drupal est un moteur de portails</li>
    <li>Concrete5 est un moteur de portails</li>
    <li>Xoops est un moteur de portails</li>
    <li>SPIP est un SGC à templates</li>
    <li>fluxBB est un moteur de forum</li>
    <li>phpBB est un moteur de forum</li>
    <li>SMF est un moteur de forum</li>
    <li>PrestaShop est un moteur de boutique en ligne</li>
    <li>DotNetNuke / DNN est un SGC libre de droits sur la plateforme Microsoft .Net</li>
    <li>Finwe est un moteur de blog</li>
    <li>Rubedo est un SGC open source multi-sites français</li>
    <li>TYPO3 est un SGC Open Source en PHP</li>
  </ul>
