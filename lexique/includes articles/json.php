    <h3>JSON :</h3>
    <p>
      JavaScript Object Notation (JSON) est un format de données textuelles dérivé de la notation des objets du langage JavaScript. Il permet de représenter de l’information structurée comme le permet XML par exemple. Créé par Douglas Crockford entre 2002 et 2005, il est décrit par la RFC 7159 de l’IETF.Un document JSON a pour fonction de représenter de l'information accompagnée d'étiquettes permettant d'en interpréter les divers éléments, sans aucune restriction sur le nombre de celles-ci.
    </p>
    <p>
      Un document JSON ne comprend que deux types d'éléments structurels :
    </p>
    <ul>
      <li>Des ensembles de paires nom / valeur ;</li>
      <li>Des listes ordonnées de valeurs.</li>
      <li>Ces mêmes éléments représentent trois types de données :</li>
      <li>Des objets ;</li>
      <li>Des tableaux ;</li>
      <li>Des valeurs génériques de type tableau, objet, booléen, nombre, chaîne ou null.</li>
    </ul>
  </p>
