  <h3>MLD</h3>
  <img src="../assets/img/mld.png" alt="image MLD" title="MLD"/>
  <p>L’étape MLD (Modèle Logique de Données) se situe chronologiquement juste après l’étape MCD et revient à présenter
    les objets du MCD sous une forme compréhensible par un SGBD. Pour faire court, dans un contexte SGBD relationnel,
    les objets représentés sont désormais des tables (disons SQL) et les liens qui les unissent.</p>
  <p> Exemples de techniques agiles : TDD, pair-programming,...</p>
