  <h3>MCD: modèle conceptuel de données</h3>
  <img src="../assets/img/mcd.png" alt="logo MCD" title="Rester enfoncé pour zoomer" />
  <h3>Définition</h3>
  <p>Le modèle conceptuel des données (MCD) a pour but d'écrire de façon formelle les données
     qui seront utilisées par le système d'information. Il s'agit donc d'une représentation des données et leurs relations, facilement
     compréhensible, permettant de décrire le système d'information à l'aide d'entités. </p>
